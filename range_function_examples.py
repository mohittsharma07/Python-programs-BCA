print("Example 1:Storing range(10) in variable")
seq = range(10) # range (stop)
for i in seq:
    print(i)

print("\nExample 2: range(10)")
for i in range(10): # range(stop)
    print(i)
   
print("\nExample 3: print range object directly")
print(range(10)) # range(stop)

print("\nExample 4: range(2,10)")
for i in range(2,10): # range(start,stop)
    print(i)

print("\nExample 5: range(2,10,2)")
for i in range(2,10,2): # raange(start,stop,step)
    print(i)

