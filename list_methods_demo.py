# 1) list.append(9)....
list = [3,7,5,2,1]
list.append(9)
print(list)

# 2) list.sort() in ascending order...
list = [3,7,5,2,1,4,6,8,9]
list.sort()
print(list)


# 3) list.sort(reverse = True) in descending order...
list =  [3,7,5,2,1,4,6,8,9]
list.sort(reverse = True)
print(list)



# 3) list.reverse()...
list =  [3,7,5,2,1,4,6,8,9]
list.reverse()
print(list)


# 4) list.insert(idx,el)...
list = [2,1,3]
list.insert(1,5)
print(list)


# 5) list.remove()...
list = [2,3,6,8,9,7]
list.remove(8)
print(list)


# 6) list.pop(idx)...
list = [4,5,3,6]
list.pop(3)
print(list)